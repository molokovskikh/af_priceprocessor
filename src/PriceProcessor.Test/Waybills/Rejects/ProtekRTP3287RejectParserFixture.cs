namespace PriceProcessor.Test.Waybills.Rejects
{
	[TestFixture]
	class ProtekRTP3287RejectParserFixture : BaseRejectFixture
	{
		/// <summary>
		/// Тест к задаче http://redmine.analit.net/issues/33351
		/// </summary>
		[Test]
		public void Parse()
		{
			//Создаем лог, а затем отказ
			var log = CreateRejectLog("35109875_Протек 1.1 РТП(refusal)_65794239_65794239.txt");
			var parser = new ProtekRTP3287RejectParser();
			var reject = parser.CreateReject(log);

			//Проверяем правильность парсинга			
			Assert.That(reject.Lines.Count, Is.EqualTo(7));
			Assert.That(parser.BadLines.Count, Is.EqualTo(0));

			//Выбираем строку и проверяем правильно ли все распарсилось
			var line = reject.Lines[0];
			Assert.That(line.Product, Is.EqualTo("КВИНАКС ГЛ. КАПЛИ 0,015% (ФЛАКОН-КАПЕЛЬНИЦЫ ПЛАСТИКОВЫЕ) 15МЛ №1"));
			Assert.That(line.Code, Is.EqualTo("3748"));
			Assert.That(line.Ordered, Is.EqualTo(3));
			Assert.That(line.Rejected, Is.EqualTo(3));
		}
	}
}
